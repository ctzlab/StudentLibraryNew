public class DTO {
    private String pdf_name;
    private String pdf_number;
    private String pdf_link;
    private String pdf_type;
    private String pdf_year;
    private String pdf_publish;



    public String getPdf_name() {
        return pdf_name;
    }

    public void setPdf_name(String pdf_name) {
        this.pdf_name = pdf_name;
    }

    public String getPdf_number() {
        return pdf_number;
    }

    public void setPdf_number(String pdf_number) {
        this.pdf_number = pdf_number;
    }

    public String getPdf_link() {
        return pdf_link;
    }

    public void setPdf_link(String pdf_link) {
        this.pdf_link = pdf_link;
    }

    public String getPdf_type() {
        return pdf_type;
    }

    public void setPdf_type(String pdf_type) {
        this.pdf_type = pdf_type;
    }

    public String getPdf_year() {
        return pdf_year;
    }

    public void setPdf_year(String pdf_year) {
        this.pdf_year = pdf_year;
    }

    public String getPdf_publish() {
        return pdf_publish;
    }

    public void setPdf_publish(String pdf_publish) {
        this.pdf_publish = pdf_publish;
    }
}
